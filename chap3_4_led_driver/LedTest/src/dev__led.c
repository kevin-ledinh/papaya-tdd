/*******************************************************************************
 *    INCLUDED FILES
 ******************************************************************************/
 #include "dev__led.h"
 
/*******************************************************************************
 *    DEFINITIONS
 ******************************************************************************/
 
/*******************************************************************************
 *    PRIVATE TYPES
 ******************************************************************************/
 
/*******************************************************************************
 *    PRIVATE DATA
 ******************************************************************************/
static uint16_t * led_addr;

/*******************************************************************************
 *    PROTOTYPES
 ******************************************************************************/
static uint16_t dev__led_convert_led_number_to_bit( int led_number );

/*******************************************************************************
*    PUBLIC FUNCTIONS
******************************************************************************/

/*****************************************************************************
 *  Function:
 *  Purpose:
*******************************************************************************/
void dev__led_init( uint16_t * address )
{
    led_addr = address;
    * led_addr = 0;
}

/*****************************************************************************
 *  Function:
 *  Purpose:
*******************************************************************************/
void dev__led_deinit( void )
{
}

/*****************************************************************************
 *  Function:
 *  Purpose:
*******************************************************************************/
void dev__led_set( int led_number )
{
    * led_addr |= dev__led_convert_led_number_to_bit( led_number );
}

/*****************************************************************************
 *  Function:
 *  Purpose:
*******************************************************************************/
void dev__led_clear( int led_number )
{
    * led_addr = 0;
}

/*******************************************************************************
 *    PRIVATE FUNCTIONS
 ******************************************************************************/

 /*****************************************************************************
 *  Function:
 *  Purpose:
*******************************************************************************/
static uint16_t dev__led_convert_led_number_to_bit( int led_number )
{
    return 1 << (led_number - 1);
}
